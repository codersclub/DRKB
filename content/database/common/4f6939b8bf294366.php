<h1>Что такое триггер?</h1>
<div class="date">01.01.2007</div>

Триггер &#8212; процедура, выполняемая сервером автоматически при модификации данных в таблице. <br>
<p>В основном, триггеры используются для поддержания целостности дублирующей информации в денормализованной БД. </p>

