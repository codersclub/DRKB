<h1>Что такое журнал транзакций?</h1>
<div class="date">01.01.2007</div>

<p>Любые изменения данных, проведенные внутри транзакции, записываются в специальный журнал транзакций (transaction log).</p>
<p>При откате транзакции, данные восстанавливаются в прежнем виде, а записи об изменениях удаляются из журнала транзакций.</p>
