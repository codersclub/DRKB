<h1>Выводить текст с помощью DBMS_OUTPUT.PUT_LINE в режиме отладки</h1>
<div class="date">01.01.2007</div>

Автор: Nomadic</p>
<p>Эта функция используется действительно только для отладки. Для того, чтобы результаты ее работы были видны из SQL Plus, необходимо в нем выдать команду:</p>
<p>set serveroutput on size 10000;</p>
