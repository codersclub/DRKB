<h1>Как получить текущее время?</h1>
<div class="date">01.01.2007</div>



<p>InterBase supports four DATE literals. They are: 'today', 'yesterday', 'tomorrow' and 'now'</p>
<p>Use it with a cast as shown in the example below.</p>

<p>insert into mytable values(cast('now' as DATE), 'Test')</p>
