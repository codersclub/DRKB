<h1>Почему IB работает неустойчиво на NetWare 4.x?</h1>
<div class="date">01.01.2007</div>


<p>Для NetWare 4.x нужно использовать IB 4.2.2. Предыдущие версии IB for Netware сертифицированы только для NetWare 3.11/3.12.</p>
<p>Для NetWare 5.x выпущена версия IB 5.6. Также нужно знать, что ни Interbase 6 ни Firebird не выпускаются и не будут выпускаться для Netware. </p>
