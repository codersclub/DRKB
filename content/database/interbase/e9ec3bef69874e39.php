<h1>Hе получается открыть таблицу (WIN1251 undefined)</h1>
<div class="date">01.01.2007</div>


<p>Hе получается открыть таблицу, созданную в InterBase с DEFAULT CHARACTER SET WIN1251. Оно говорит, что "WIN1251 undefined".</p>

<p>Ставьте Interbase в каталог с путем, соответствующим DOS-овским соглашениям об именах (8+3).</p>

