<h1>Как сменить пароль для таблицы Paradox?</h1>
<div class="date">01.01.2007</div>


<p>Нет способа сделать это в пределах Delphi VCL. Кажется, это довольно серьезное упущение. Однако, есть возможность сделать это напрямую через Borland Database Engine через интерфейс предоставляемый модулями DBIPROCS и DBITYPES. Нужно использовать DbiDoRestructure.</p>
