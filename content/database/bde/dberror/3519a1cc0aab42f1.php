<h1>BDE Error 2B04 Too many open files</h1>
<div class="date">01.01.2007</div>



<p>Open the tool 'BDE Administrator' and choose the right tab 'Configuration'. There open the node 'System' and click on subnode 'INIT'. Then update 'MaxFileHandles' on the right side (it is 48 by default).</p>
