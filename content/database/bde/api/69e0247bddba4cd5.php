<h1>Управление транзакциями</h1>
<div class="date">01.01.2007</div>


<p>Each function listed below begins, ends, or inquires about the status of a transaction.</p>


<p>DbiBeginTran:</p>
<p>Begins a transaction.</p>

<p>DbiEndTran:</p>
<p>Ends a transaction.</p>

<p>DbiGetTranInfo:</p>
<p>Retrieves the transaction state.</p>
