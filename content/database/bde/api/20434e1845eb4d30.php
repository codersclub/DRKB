<h1>Работа с базами данных</h1>
<div class="date">01.01.2007</div>


<p>Each function listed below returns information about a specific database, available databases, or performs a database-related task, such as opening or closing a database.</p>


<p>DbiCloseDatabase:</p>
<p>Closes a database and all tables associated with this database handle.</p>

<p>DbiGetDatabaseDesc:</p>
<p>Retrieves the description of the specified database from the configuration file.</p>

<p>DbiGetDirectory:</p>
<p>Retrieves the current working directory or the default directory.</p>

<p>DbiOpenDatabase:</p>
<p>Opens a database in the current session and returns a database handle.</p>

<p>DbiOpenDatabaseList:</p>
<p>Creates an in-memory table containing a list of accessible databases and their descriptions.</p>

<p>DbiOpenFileList:</p>
<p>Opens a cursor on the virtual table containing all the tables accessible by the client application</p>
<p>and their descriptions.</p>

<p>DbiOpenIndexList:</p>
<p>Opens a cursor on an in-memory table listing the indexes on a specified table, along with</p>
<p>their descriptions.</p>

<p>DbiOpenTableList:</p>
<p>Creates an in-memory table with information about all the tables accessible to the client application.</p>
