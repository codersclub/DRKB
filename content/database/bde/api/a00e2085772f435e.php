<h1>Работа с индексами</h1>
<div class="date">01.01.2007</div>


<p>Each function listed below returns information about an index or indexes, or performs a task that affects an index, such as dropping it, deleting it, or adding it.</p>


<p>DbiAddIndex:</p>
<p>Creates an index on an existing table.</p>

<p>DbiCloseIndex:</p>
<p>Closes the specified index on a cursor.</p>

<p>DbiCompareKeys:</p>
<p>Compares two key values based on the current index of the cursor.</p>

<p>DbiDeleteIndex:</p>
<p>Drops an index on a table.</p>

<p>DbiExtractKey:</p>
<p>Retrieves the key value for the current record of the given cursor or from the supplied record buffer.</p>

<p>DbiGetIndexDesc:</p>
<p>Retrieves the properties of the given index associated with the cursor.</p>

<p>DbiGetIndexDescs:</p>
<p>Retrieves index properties.</p>

<p>DbiGetIndexForField:</p>
<p>Returns the description of any useful index on the specified field.</p>

<p>DbiGetIndexSeqNo:</p>
<p>Retrieves the ordinal number of the index in the index list of the specified cursor.</p>

<p>DbiGetIndexTypeDesc:</p>
<p>Retrieves a description of the index type.</p>

<p>DbiOpenIndex:</p>
<p>Opens the index for the table associated with the cursor.</p>

<p>DbiRegenIndex:</p>
<p>Regenerates an index to make sure that it is up-to-date (all records currently in the table</p>
<p>are included in the index and are in the index order).</p>

<p>DbiRegenIndexes:</p>
<p>Regenerates all out-of-date indexes on a given table.</p>

<p>DbiSwitchToIndex:</p>
<p>Allows the user to change the active index order of the given cursor.</p>
