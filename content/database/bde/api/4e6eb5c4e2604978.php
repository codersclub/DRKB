<h1>Схемы и возможности</h1>
<div class="date">01.01.2007</div>


<p>Each function listed below returns information about capabilities or the schema.</p>


<p>DbiOpenCfgInfoList:</p>
<p>Returns a handle to an in-memory table listing all the nodes in the configuration file accessible by</p>
<p>the specified path.</p>

<p>DbiOpenDatabaseList:</p>
<p>Creates an in-memory table containing a list of accessible databases and their descriptions.</p>

<p>DbiOpenDriverList:</p>
<p>Creates an in-memory table containing a list of driver names available to the client application.</p>

<p>DbiOpenFamilyList:</p>
<p>Creates an in-memory table listing the family members associated with a specified table.</p>

<p>DbiOpenFieldList:</p>
<p>Creates an in-memory table listing the fields in a specified table and their descriptions.</p>

<p>DbiOpenFieldTypesList:</p>
<p>Creates an in-memory table containing a list of field types supported by the table type for the driver type.</p>

<p>DbiOpenFunctionArgList:</p>
<p>Returns a list of arguments to a data source function.</p>

<p>DbiOpenFunctionList:</p>
<p>Returns a description of a data source function.</p>

<p>DbiOpenIndexList:</p>
<p>Opens a cursor on an in-memory table listing the indexes on a specified table, along with</p>
<p>their descriptions.</p>

<p>DbiOpenIndexTypesList:</p>
<p>Creates an in-memory table containing a list of all supported index types for the driver type.</p>

<p>DbiOpenLockList:</p>
<p>Creates an in-memory table containing a list of locks acquired on the table.</p>

<p>DbiOpenRintList :</p>
<p>Creates an in-memory table listing the referential integrity links for a specified table, along with</p>
<p>their descriptions.</p>

<p>DbiOpenSecurityList:</p>
<p>Creates an in-memory table listing record-level security information about a specified table.</p>

<p>DbiOpenTableList:</p>
<p>Creates an in-memory table with information about all the tables accessible to the client application.</p>

<p>DbiOpenTableTypesList:</p>
<p>Creates an in-memory table listing table type names for the given driver.</p>

<p>DbiOpenVchkList:</p>
<p>Creates an in-memory table containing records with information about validity checks for fields</p>
<p>within the specified table.</p>
