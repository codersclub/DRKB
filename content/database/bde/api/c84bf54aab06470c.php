<h1>Обработка исключений</h1>
<div class="date">01.01.2007</div>


<p>Each function listed below returns error handling information or performs a task that relates to error handling.</p>


<p>DbiGetErrorContext:</p>
<p>After receiving an error code back from a call, enables the client to probe BDE for more specific</p>
<p>error information.</p>

<p>DbiGetErrorEntry:</p>
<p>Returns the error description of a specified error stack entry.</p>

<p>DbiGetErrorInfo:</p>
<p>Provides descriptive error information about the last error that occurred.</p>

<p>DbiGetErrorString:</p>
<p>Returns the message associated with a given error code.</p>
