<h1>Limit reached for installs using Kylix serial number</h1>
<div class="date">01.01.2007</div>


<p>I am attempting to register Kylix, but cannot and get the following error:</p>
<p>--------------------</p>
<p>Error registering software. The limit has been reached for installs using this serial number, please contact customer service to increase the allowed install count.</p>
<p>--------------------</p>
<p>How do I get around this and register Kylix?&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>Borland has a registration server that keeps count of how many times a product has been registered. The default count is five registrations. This error indicates that your serial number has already been registered five times, and the registration currently being attempted exceeds the count.</p>
<p>You can get this count increased by calling Borland's registration hotline at (888) 588-2230. Explain to the representative who answers the phone what product you have, as well as the full error message, and he or she (after asking for your relevant information) will be able to increase the count to allow for additional registrations.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
