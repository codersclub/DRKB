<h1>Как отлаживать консольные приложения?</h1>
<div class="date">01.01.2007</div>


<p>As with Delphi you can use Kylix to write console applications even though many</p>
<p>people think that's not important. ;-)</p>
<p>When you start a console program in the Delphi Debugger it automatically opens</p>
<p>a console window ("DOS command prompt") where you can see the output of e.g.</p>
<p>the writeln command.</p>
<p>Kylix doesn't do that automatically and if you don't look hard enough you might</p>
<p>think it is impossible to debug console applications with it.</p>
<p>But if you open the run / parameters dialog you will find an entry called</p>
<p>"Use Launcher Application" that is prefilled with xconsole. Just tick this</p>
<p>option and there you go.</p>
