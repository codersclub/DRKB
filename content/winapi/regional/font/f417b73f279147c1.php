<h1>Как использовать неустановленный шрифт?</h1>
<div class="date">01.01.2007</div>


<p>Зарегистрировать шрифт:</p>
<p>AddFontResource('путь к фонту\\Algerian.ttf');</p>
<p>Объект.Font.Name:="Algerian";</p>
<p>Удалить -</p>
<p>RemoveFontResource('путь к фонту\\Algerian.ttf');</p>
