<h1>Как включить / выключить спикер?</h1>
<div class="date">01.01.2007</div>



<p>Это выключит спикеp:</p>
<p>  SyStemParametersInfo(SPI_SETBEEP,0,nil,SPIF_UPDATEINIFILE);</p>

<p>Это включит:</p>
<p>  SyStemParametersInfo(SPI_SETBEEP,1,nil,SPIF_UPDATEINIFILE);</p>

