<p>Веббраузер это Microsoft's Internet Explorer в виде ActiveX контрола.</p>
<p>Его можно импортировать в Delphi IDE и размещать на форме на равне с другими компонентами. Поэтому, чтобы превратить Ваше приложение в браузер, достаточно воспользоваться всей мощью IE.</p>

