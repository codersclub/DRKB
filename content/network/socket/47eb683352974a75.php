<h1>Где TServerSocket и TClientSocket в Delphi 7?</h1>
<div class="date">01.01.2007</div>


<p>The TClientSocket and TServerSocket components seem to be missing from my installation of Delphi 7?</p>

<p>You will need to add the dclsockets package to the IDE. To do this go to Component | Install Packages | Add (/bin/dclsockets70.bpl).&nbsp;</p>
