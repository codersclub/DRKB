<h1>В основном help'е в Delphi не работает индекс по Win32</h1>
<div class="date">01.01.2007</div>


<p>- в /help/delphi3.cfg добавить строку типа</p>
<p>   :index Win32=Win32.hlp</p>
<p>   она должна быть добавлена перед строкой</p>
<p>   :Link win32.hlp</p>
<p>   - стереть delphi3.gid</p>
<p>   - запустить Help и получать удовольствие</p>

<p>   В delphi3.cnt тоже нужно строчку добавить:</p>
<p>   :include win32.cnt</p>
