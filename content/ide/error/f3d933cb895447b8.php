<h1>Список префиксов Internal Error</h1>
<div class="date">01.01.2007</div>


<p>Иногда выскакивают ошибки типа "Internal Error SY424", вот расшифровка префиксов:</p>

<p>B : debugger</p>
<p>BC : debugger</p>
<p>BR : browser</p>
<p>C : codegen</p>
<p>CM : command line version of the compiler</p>
<p>D : parser</p>
<p>DB : debugger</p>
<p>DBG: debug info output</p>
<p>DM : IDE version of the compiler</p>
<p>E : parser</p>
<p>EO : debugger/evaluator</p>
<p>FN : filename / pathname parsing</p>
<p>GH : HPP generator</p>
<p>I : code generator</p>
<p>IN : inspectors</p>
<p>L : linker</p>
<p>LI : BPI file writing</p>
<p>LO : object file loading</p>
<p>M : memory allocation</p>
<p>MA : name mangling</p>
<p>MB : multi-byte (MBCS) support</p>
<p>O : object (OMF) file handling</p>
<p>P : package managment</p>
<p>R : resource writing</p>
<p>S : scanner</p>
<p>ST : standard procedure handling</p>
<p>SY : symbol table</p>
<p>T : code generator</p>
<p>TI : RTTI generator</p>
<p>U : parser</p>
<p>UD : IDE version of the compiler</p>
<p>UI : error handling</p>
<p>URW: DCU reading/writing</p>
<p>W : Object file (OMF) writing</p>
<p>X : code generator</p>

