<h1>Информация о проекте</h1>

  	<p><strong>DRKB</strong> (<b>D</b>elphi <b>R</b>ussian <b>K</b>nowledge <b>B</b>ase) — 
  	это самая большая и удобная в использовании база знаний по Дельфи в рунете, составленная Виталием Невзоровым.</p>
  	<p><strong>DRKB</strong> является одним из самых известных и полных <strong>Delphi FAQ</strong> на русском языке.</p>
  	<p>База знаний составлена по материалам форумов <a href="https://forum.vingrad.ru/" title="Перейти на форумы Vingrad" target="_blank">Vingrad</a> и <a href="https://forum.sources.ru/" title="Перейти на форумы Исходников" target="_blank">Sources.ru</a>, а также многих других авторитетных источников. База содержит более <strong>4000</strong> хорошо классифицированных и тщательно оформленных статей.</p>
        <p>DRKB распространяется в двух форматах:</p>
        <ul>
              <li>В формате <strong>CHM</strong> (Windows Help). Посмотреть <a href="/img/screen5.png" title="Скриншот #5: DRKB, CHM-формат" target="_blank">скриншот</a>.</li>
              <li>В формате <strong>Online</strong>.</li>
        </ul>
        <p></p>
  	<p>DRKB — абсолютно бесплатный проект. Вы можете совершенно свободно качать базу знаний, использовать любые статьи и  коды в  любых целях, в &nbsp;том числе и&nbsp;для&nbsp;применения в&nbsp;коммерческих приложениях.<br>Никакой оплаты, временных ограничений (trial), shareware или&nbsp;рекламы.
        <br>Единственная просьба - указывайте ссылку на первоначальный источник совета или&nbsp;статьи.</p>





