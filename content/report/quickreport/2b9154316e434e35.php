<h1>Позволяет ли Quick Report выгружать данные в формате Microsoft Excel?</h1>
<div class="date">01.01.2007</div>


<p>Позволяет ли QuickReport выгружать данные в формате Microsoft Excel?</p>

<p>Quick Report не позволяет выгружать данные в формате Microsoft Excel. Но последние его версии позволяют сохранять отчеты в формате CSV (Comma Separated Value) и HTML, и оба эти формата можно прочесть с помощью Excel.</p>

<p>Помимо этого, для генерации отчета можно использовать автоматизацию Excel (Automation, ранее назвалось OLE Automation. &#8212; Прим. ред.), вообще не прибегая к использованию QuickReport.</p>

