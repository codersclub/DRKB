<h1>Арифметика</h1>
<div class="date">01.01.2007</div>


<p>Abs &nbsp; &nbsp; &nbsp; &nbsp;Возвращает абсолютное значение аргумента. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Ceil &nbsp; &nbsp; &nbsp; &nbsp;Округляет значение аргумента в большую сторону. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Exp &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет значение ex. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Floor &nbsp; &nbsp; &nbsp; &nbsp;Округляет значение аргумента в меньшую сторону. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Frac &nbsp; &nbsp; &nbsp; &nbsp;Возвращает дробную часть аргумента. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Frexp &nbsp; &nbsp; &nbsp; &nbsp;Возвращает мантиссу и экспоненту аргумента. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Int &nbsp; &nbsp; &nbsp; &nbsp;Возвращает целую часть аргумента. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>IntPower &nbsp; &nbsp; &nbsp; &nbsp;Возводит аргумент X в целочисленную степень Y. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Ldexp &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет X*2Y. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Ln &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет натуральный логарифм ln(x). &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>LnXP1 &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет натуральный логарифм ln(x+1). &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Log10 &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет десятичный логарифм. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Log2 &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет логарифм аргумента по основанию 2. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>LogN &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет логарифм аргумента по основанию N. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Max &nbsp; &nbsp; &nbsp; &nbsp;Возвращает большее из двух чисел. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Min &nbsp; &nbsp; &nbsp; &nbsp;Возвращает меньшее из двух чисел. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Pi &nbsp; &nbsp; &nbsp; &nbsp;Возвращает значение числа Пи. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Poly &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет однородный полином. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Power &nbsp; &nbsp; &nbsp; &nbsp;Возводит X в степень Y. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Round &nbsp; &nbsp; &nbsp; &nbsp;Округляет число к ближайшему целому. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Sqr &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет квадрат аргумента Х. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Sqrt &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет квадратный корень аргумента Х. &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>Trunc &nbsp; &nbsp; &nbsp; &nbsp;Отсекает дробную часть числа. &nbsp; &nbsp; &nbsp; &nbsp;</p>
