<h1>Функции и процедуры для финансовых вычислений</h1>
<div class="date">01.01.2007</div>


<p>DoubleDecliningBalance Вычисляет амортизационные отчисления на определенном этапе.</p>
<p>FutureValue  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет значение вклада через определенный период времени.</p>
<p>InterestPayment  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет процентную ставку кредита.</p>
<p>InterestRate  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет процентную ставку инвестиций.</p>
<p>InternalRateOfReturn  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет внутреннюю ставку дохода от инвестиций.</p>
<p>NetPresentValue  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет текущее значение платежа, используя массив с расчетными значениями.</p>
<p>NumberOfPeriods  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет количество этапов необходимое для погашения кредита.</p>
<p>Payment  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет полную сумму погашения кредита на отдельном этапе.</p>
<p>PeriodPayment  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет необходимую сумму выплат основной части (без процентов) кредита на определенном этапе.</p>
<p>PresentValue  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Определяет текущее значение вклада.</p>
<p>SLNDepreciation  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет сумму амортизационных отчислений по методу равномерной амортизации.</p>
<p>SYDDepreciation  &nbsp; &nbsp; &nbsp; &nbsp;Вычисляет сумму амортизационных отчислений по методу ускоренной амортизации.</p>
