<h1>Проблемы с дробными числами</h1>
<div class="date">01.01.2007</div>


<p>Иногда возникают трудности интерпретации дробных чисел - что есть разделитель точка или запятая?</p>
<p>В Дельфи есть системные переменные:</p>
<p>DECIMALSEPARATOR - десятичный разделитель который принят в системе</p>
<p>THOUSANDSEPARATOR - разделитель тысяч, который принят в системе</p>
<p>Для USA регионального стандарта</p>
<p>DECIMALSEPARATOR будет "."</p>
<p>THOUSANDSEPARATOR будет ","</p>
<p>Для России</p>
<p>DECIMALSEPARATOR будет ","</p>
<p>THOUSANDSEPARATOR будет "." или " " (не помню уже) </p>
