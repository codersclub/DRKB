<h1>Функции и процедуры для работы с данными перечислимого типа</h1>
<div class="date">01.01.2007</div>


<p>Dec  &nbsp; &nbsp; &nbsp; &nbsp;Уменьшает значение переменной на заданную величину. </p>
<p>Inc  &nbsp; &nbsp; &nbsp; &nbsp;Увеличивает значение переменной на заданную величину. </p>
<p>Odd  &nbsp; &nbsp; &nbsp; &nbsp;Определяет четность аргумента. </p>
<p>Ord  &nbsp; &nbsp; &nbsp; &nbsp;Возвращает порядковый номер выражения перечислимого типа или код ASCII выражения символьного типа. </p>
<p>Pred  &nbsp; &nbsp; &nbsp; &nbsp;Возвращает значение, предшествующее аргументу. </p>
<p>Succ  &nbsp; &nbsp; &nbsp; &nbsp;Возвращает значение, следующее за аргументом. </p>
