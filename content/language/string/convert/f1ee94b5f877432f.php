<h1>Функции преобразования UNICODE и ANSI строк</h1>
<div class="date">01.01.2007</div>


<p>StringToWideChar         Преобразовывает строку формата ANSI в Unicode-строку.</p>
<p>WideCharLenToString Преобразовывает указанное количество символов Unicode-строки в ANSI строку.</p>
<p>WideCharLenToStrVar Преобразовывает заданное количество символов Unicode-строки в ANSI формат и копирует результат в указанную переменную.</p>
<p>WideCharToString Преобразовывает длинную строку Unicode в ANSI строку.</p>
<p>WideCharLenToStrVar Преобразовывает строку формата Unicode в ANSI-формат и копирует результирующую строку в указанную переменную.</p>

