<h1>Математика времени</h1>
<div class="date">01.01.2007</div>

<p>Работа с временными величинами в Delphi очень проста, если пользоваться встроенными функциями преобразования. Определите глобальные Hour, Minute, Second и инициализируйте их следующим образом:</p>
<pre>
Hour   := EncodeTime(1,0,0,0);
Minute := EncodeTime(0,1,0,0);
Second := EncodeTime(0,0,1,0);
</pre>
<p>Или, если вы предпочитаете константы, сделайте так:</p>
<pre>
Hour = 3600000/MSecsPerDay;
Minute = 60000/MSecsPerDay;
Second = 1000/MSecsPerDay;
</pre>
<p>Теперь для того, чтобы добавить 240 минут к переменной TDateTime, просто сделайте</p>
<p>T := T + 240*Minute;</p>
