<h1>Deploying Midas</h1>
<div class="date">01.01.2007</div>


<p>You need to purchase a MIDAS license for the application server. When you purchase a MIDAS license you receive a CD with the MIDAS suite for install.&nbsp; You need to install the</p>
<p>EXE's and DLL's that you created for your app, the rest (which also includes the runtime packages, BDE, SQL Links, etc.) are installed by the MIDAS software.</p>

