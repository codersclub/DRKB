<h1>Как быть, если таблица содержит столбцы, количество строк в которых разное?</h1>
<div class="date">01.01.2007</div>

<p>Внести текст в ячейку можно, только если все ячейки одинаковой ширины и высоты. Иначе попросту OLE выдаст ошибку. Как быть, если таблица содержит столбцы, количество строк в которых разное?</p>
Я использую перемещение от одной ячейки к следующей или поиск по шаблону. Затем заполняю выделенную ячейку информацией.</p>

