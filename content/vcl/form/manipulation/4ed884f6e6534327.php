<h1>В каком порядке происходят события при создании и показе окна?</h1>
<div class="date">01.01.2007</div>


<p>При создании окна обработчики событий выполняются в следующем порядке:</p>

<p>        OnCreate</p>
<p>        OnShow</p>
<p>        OnPaint</p>
<p>        OnActivate</p>
<p>        OnResize</p>
<p>        OnPaint (снова)</p>

