<h1>Как скрыть свое приложение?</h1>
<div class="date">01.01.2007</div>


<p>Если Вы хотет, чтобы Ваша программа не появлялась на панели задач, но форма была видна, то воспользуйтесь следующей командой:</p>
<pre>
ShowWindow (Application.handle, SW_HIDE); 
</pre>

<p>Данная команда в основном используется в приложениях, которае активируются через иконку в трее.</p>

