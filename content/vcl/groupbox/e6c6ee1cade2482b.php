<h1>Как заставить TGroupBox прорисовать на форме свой Caption неактивным цветом?</h1>
<div class="date">01.01.2007</div>


<p>Как заставить GroupBox1 прорисовать на форме свой Caption неактивным цветом? GroupBox1.Enabled:=FALSE не помогает. Хотя если то же самое проделать с Label1 или Edit1, то все получается.</p>
<pre>
GroupBox1.Font.color:=clInactiveCaption;
</pre>
