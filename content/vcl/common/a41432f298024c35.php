<h1>Как различаются между собой Paint-события: Invalidate, Update и Refresh?</h1>
<div class="date">01.01.2007</div>


<p>Invalidate весь объект перерисовывается заново; обычно перерисовывается только часть бывшая перед этим закрытой</p>

<p>Update  незамедлительная перерисовка</p>

<p>Refresh  Invalidate + Update</p>

